import { Role } from '@prisma/client';

/**
 * Один Telegram-аккаунт = одна личность в системе.
 *
 * `User.telegramId` несёт ровно один смысл — «кто это». Он же служит и якорем входа
 * в мобилку, и адресом кабинета продавца в боте, поэтому совмещать в одном
 * Telegram роль покупателя и роль staff'а **запрещено в обе стороны**:
 *
 * - покупатель уже вошёл в мобилку → привязать его Telegram к аккаунту продавца нельзя;
 * - Telegram привязан к staff-аккаунту → войти под ним в мобилку нельзя.
 *
 * Без этого запрета получались две беды. В одну сторону — unique-конфликт на
 * `telegramId` с тупиком (logout не освобождает поле). В другую, что хуже, —
 * молчаливое слияние: мобилка выдавала токены на staff-аккаунт, заказы и телефон
 * покупателя ложились в учётку продавца, отдельной покупательской личности
 * не возникало вовсе.
 *
 * Кому нужны обе роли — заводит второй Telegram-аккаунт под магазин.
 */
export const isStaffRole = (role: Role): boolean =>
  role === Role.SELLER || role === Role.SUPER_ADMIN;

/** Показывается в боте при попытке войти в мобилку под staff-аккаунтом. */
export const STAFF_CANNOT_SHOP =
  'Этот Telegram привязан к рабочему аккаунту (продавец/администратор). ' +
  'Войти в приложение как покупатель под ним нельзя — используйте другой Telegram-аккаунт.';

/** Показывается при попытке привязать Telegram, который уже вошёл в мобилку. */
